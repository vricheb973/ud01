package operadores;

public class OperaAsigna {
	
	public static void main(String[] args) {
		
		int numero = 0;
		
		numero = numero + 3;
		//son equivalentes
		numero += 3;
		
		numero = numero * 2;
		//son equivalentes
		numero *= 2;
		
		numero = numero - 4;
		//son equivalentes
		numero -= 4;
		
		numero = numero / 2;
		//son equivalentes
		numero /= 2;
		
		numero = numero % 3;
		//son equivalentes
		numero %= 3;
		
	}

}
